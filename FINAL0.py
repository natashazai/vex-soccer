class FindGoalBarrels(StateNode):
	def start(self, event=None):
		super().start(event)
		barrels = []
		for obj in robot.world_map.objects.values():

			if isinstance(obj, BarrelObj) and obj.is_visible:
				barrels.append(obj)

		if len(barrels) >= 2:
			robot.goal_barrel_1 = barrels[0]
			robot.goal_barrel_2 = barrels[1]

			self.post_success()
			return

		self.post_failure()

class ComputeGoalMidpoint(StateNode):
	def start(self, event=None):
		super().start(event)

		barrel1 = robot.goal_barrel_1
		barrel2 = robot.goal_barrel_2

		mid_x = (barrel1.pose.x + barrel2.pose.x) / 2
		mid_y = (barrel1.pose.y + barrel2.pose.y) / 2

		robot.goal_midpoint = (mid_x, mid_y)

		self.post_success()

class TurnTowardGoal(StateNode):
	def start(self, event=None):
		super().start(event)

		goal_x, goal_y = robot.goal_midpoint

		robot_x = robot.pose.x
		robot_y = robot.pose.y

		dist_x = goal_x - robot_x
		dist_y = goal_y - robot_y

		target_heading = degrees(atan2(dist_y, dist_x))

		current_heading = degrees(robot.pose.theta)

		turn_angle = target_heading - current_heading

		while turn_angle > 180:
			turn_angle -= 360

		while turn_angle < -180:
			turn_angle += 360

		self.turn = Turn(turn_angle)

		self.turn.set_parent(self)
		self.turn.start()

	def child_completed(self, child, event):
		self.post_completion()

class FindBall(StateNode):
	def start(self, event=None):
		super().start(event)

		ball = None

		# Search for visible sports ball
		for obj in robot.world_map.objects.values():

			if isinstance(obj, SportsBallObj) and obj.is_visible:

				ball = obj
				break
		if ball is None:

			print("Could not find sports ball")

			self.post_failure()
			return

		robot.target_ball = ball

		self.robot.path_planner.plan_path_this_process(
			self.robot,
			robot.target_ball
		)

		self.robot.rrt.draw_path = self.robot.rrt.path

		self.post_success()

