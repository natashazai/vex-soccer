class GoalCheck(StateNode):
	goals_scored = 0
	def start(self, event=None):
		super().start(event)

		ball = None
		for obj in robot.world_map.objects.values():
			if isinstance(obj, SportsBallObj) and obj.is_visible:
				ball = obj
				break

		if ball is None:
			self.post_failure()
			return

		b1 = robot.goal_barrel_1
		b2 = robot.goal_barrel_2

		min_x = min(b1.pose.x, b2.pose.x)
		max_x = max(b1.pose.x, b2.pose.x)

		if (min_x <= ball.pose.x <= max_x):

			GoalCheck.goals_scored += 1

			Say("Goals scored", {GoalCheck.goals_scored})
			print("Goals scored:", GoalCheck.goals_scored)

			self.post_success()
		else:
			self.post_failure()