class TurnToBall(StateNode):
	def start(self, event=None):
		super().start(event)

		current_heading = degrees(robot.pose.theta)
		turn_angle = robot.drive_angle - current_heading
		while turn_angle > 180:
			turn_angle -= 360
		while turn_angle < -180:
			turn_angle += 360

		self.turn = Turn(turn_angle)
		self.turn.set_parent(self)
		self.turn.start()

	def child_completed(self, child, event):
		self.post_completion()

